package Model;

import java.sql.Date;

/**
 *
 * @author phung
 */
public class Course {
    private int id;
    private String name;
    private Date start;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getStart() {
        return start;
    }

    public void setStart(Date start) {
        this.start = start;
    }

    
    
}
